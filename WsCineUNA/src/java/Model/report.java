package Model;

/**
 *
 * @author mario
 */
public class report {
    String nombre;
    String tanda;
    Integer cantidad;
    String fecha;

    public report() {
    }

    public report(String nombre, String tanda, Integer cantidad, String fecha) {
        this.nombre = nombre;
        this.tanda = tanda;
        this.cantidad = cantidad;
        this.fecha = fecha;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getTanda() {
        return tanda;
    }

    public void setTanda(String tanda) {
        this.tanda = tanda;
    }

    public Integer getCantidad() {
        return cantidad;
    }

    public void setCantidad(Integer cantidad) {
        this.cantidad = cantidad;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

  
  
}
